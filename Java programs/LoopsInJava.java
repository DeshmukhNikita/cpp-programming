//Pyramid Example 1:

public class LoopsInJava{
	
	public static void main(String args[])
	{
		for(int i=5;i<=10;i++)
		{
			for(int j=i-1;j<10;j++)
				
				{ 
					System.out.print("*");
				}
				System.out.println("");
				
		}
	}
}
	
		

	

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		